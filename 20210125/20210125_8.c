#include <stdio.h>
/*Задача 8. Използвайте функцията getchar(), с която въвеждате текст от клавиатурата, 
докато не подадете ЕОF(Ctrl+D). Текстът има букви и цифри. Принтирайте с putchar(). 
Използвайте условните оператори, за да премахнете шпациите и табулациите в стринга*/

int main(void){
    char text;

    while((text = getchar()) != EOF){
        if ((text == ' ') || (text == '\t')){
            continue;
        }
        putchar(text);
    }
}